
public class Process {
	public String name;
	public int level;
	public int computingTime;
	public int arrival;
	
	public Process(String name, int level, int computingTime, int arrival) {
		super();
		this.name = name;
		this.level = level;
		this.computingTime = computingTime;
		this.arrival = arrival;
	}
}
